-- ============================================================
-- Exploratory Data Analysis (EDA) Project
-- Purpose: Understand the structure, content, and business
--          insights from the DataWarehouse database.
-- ============================================================


-- ============================================================
-- 1. Database & Schema Exploration
-- ============================================================

-- View all schemas in the database
SHOW SCHEMAS;

-- View all tables available in all schemas
SELECT * FROM information_schema.tables;

-- View all databases
SHOW DATABASES;


-- ============================================================
-- 2. Table & Column Exploration
-- ============================================================

-- Switch to the main working database
USE DataWarehouse;

-- View all tables in the database
SHOW TABLES;

-- View all columns in the dim_customers table
SELECT * 
FROM information_schema.columns 
WHERE table_name = 'dim_customers';


-- ============================================================
-- 3. Dimension Exploration
-- ============================================================

-- Find all countries where customers come from
SELECT DISTINCT country 
FROM gold.dim_customers;

-- Find all product categories
SELECT DISTINCT category 
FROM gold.dim_products;

-- Find category and subcategory combinations
SELECT DISTINCT category, subcategory 
FROM gold.dim_products 
ORDER BY category, subcategory DESC;

-- Find category, subcategory, and product names
SELECT DISTINCT category, subcategory, product_name 
FROM gold.dim_products 
ORDER BY category, subcategory, product_name DESC;


-- ============================================================
-- 4. Date Exploration
-- ============================================================

-- Find the first and last order dates
SELECT 
    MIN(order_date) AS first_order_date,
    MAX(order_date) AS last_order_date
FROM gold.fact_sales;

-- Find the oldest and youngest customers (by birthdate)
SELECT 
    MIN(birthdate) AS oldest_customer_birthdate,
    MAX(birthdate) AS youngest_customer_birthdate
FROM gold.dim_customers;

-- Find the age of the oldest and youngest customers
SELECT 
    TIMESTAMPDIFF(YEAR, MIN(birthdate), NOW()) AS oldest_age,
    TIMESTAMPDIFF(YEAR, MAX(birthdate), NOW()) AS youngest_age
FROM gold.dim_customers;


-- ============================================================
-- 5. Measure Exploration (Key Business Metrics)
-- ============================================================

-- Total sales revenue
SELECT SUM(sales_amount) AS total_sales 
FROM gold.fact_sales;

-- Total quantity of items sold
SELECT SUM(quantity) AS total_quantity 
FROM gold.fact_sales;

-- Average selling price
SELECT AVG(Asprice) AS avg_price 
FROM gold.fact_sales;

-- Total number of orders (all rows)
SELECT COUNT(*) AS total_orders 
FROM gold.fact_sales;

-- Total number of unique orders
SELECT COUNT(DISTINCT order_number) AS total_orders 
FROM gold.fact_sales;

-- Total number of customers
SELECT COUNT(customer_key) AS total_customers 
FROM gold.dim_customers;

-- Total number of customers who placed orders
SELECT COUNT(DISTINCT customer_key) AS total_customers_ordered 
FROM gold.fact_sales;

-- Summary report of all key measures
SELECT 'Total_sales' AS measure_name, SUM(sales_amount) AS measure_value FROM gold.fact_sales
UNION ALL
SELECT 'Total_quantity', SUM(quantity) FROM gold.fact_sales
UNION ALL
SELECT 'Avg_price', AVG(Asprice) FROM gold.fact_sales
UNION ALL
SELECT 'Total_orders', COUNT(DISTINCT order_number) FROM gold.fact_sales
UNION ALL
SELECT 'Total_products', COUNT(product_name) FROM gold.dim_products
UNION ALL
SELECT 'Total_customers', COUNT(customer_key) FROM gold.dim_customers;


-- ============================================================
-- 6. Magnitude Analysis (Category-wise, Country-wise)
-- ============================================================

-- Total number of customers by country
SELECT country, COUNT(customer_key) AS total_customers 
FROM gold.dim_customers
GROUP BY country
ORDER BY total_customers DESC;

-- Total number of customers by gender
SELECT gender, COUNT(customer_key) AS total_customers 
FROM gold.dim_customers
GROUP BY gender
ORDER BY total_customers DESC;

-- Total number of products by category
SELECT category, COUNT(product_key) AS total_products 
FROM gold.dim_products
GROUP BY category
ORDER BY total_products DESC;

-- Average cost of products by category
SELECT category, ROUND(AVG(cost), 1) AS avg_cost 
FROM gold.dim_products
GROUP BY category
ORDER BY avg_cost DESC;

-- Total revenue generated by each category
SELECT 
    p.category,
    SUM(f.sales_amount) AS total_revenue 
FROM gold.fact_sales f
LEFT JOIN gold.dim_products p 
ON f.product_key = p.product_key
GROUP BY p.category
ORDER BY total_revenue DESC;

-- Total revenue generated by each customer
SELECT 
    c.customer_key,
    c.first_name,
    c.last_name,
    SUM(f.sales_amount) AS total_revenue  
FROM gold.fact_sales f 
LEFT JOIN gold.dim_customers c
ON c.customer_key = f.customer_key
GROUP BY c.customer_key, c.first_name, c.last_name
ORDER BY total_revenue DESC;

-- Distribution of items sold across countries
SELECT 
    c.country,
    SUM(f.quantity) AS total_quantity  
FROM gold.fact_sales f 
LEFT JOIN gold.dim_customers c
ON c.customer_key = f.customer_key
GROUP BY c.country
ORDER BY total_quantity DESC;


-- ============================================================
-- 7. Rank Analysis
-- ============================================================

-- Top 5 products by total revenue
SELECT 
    p.product_name,
    SUM(f.sales_amount) AS total_revenue 
FROM gold.fact_sales f 
LEFT JOIN gold.dim_products p
ON p.product_key = f.product_key
GROUP BY p.product_name
ORDER BY total_revenue DESC
LIMIT 5;

-- Bottom 5 products by total revenue
SELECT 
    p.product_name,
    SUM(f.sales_amount) AS total_revenue 
FROM gold.fact_sales f 
LEFT JOIN gold.dim_products p
ON p.product_key = f.product_key
GROUP BY p.product_name
ORDER BY total_revenue ASC
LIMIT 5;

-- Rank products based on revenue using window function
SELECT * 
FROM (
    SELECT 
        p.product_name,
        SUM(f.sales_amount) AS total_revenue,
        ROW_NUMBER() OVER (ORDER BY SUM(f.sales_amount) DESC) AS rank_products
    FROM gold.fact_sales f 
    LEFT JOIN gold.dim_products p
    ON p.product_key = f.product_key
    GROUP BY p.product_name
) t
WHERE rank_products <= 5;
